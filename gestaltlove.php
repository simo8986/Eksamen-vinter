        <h3>Proximity</h3>
                <p>Objekter som er tætte opfattes som at være relaterede i højere grad end objekter der er sat langt fra hinanden. Nærhed ’trumfer’ lighed i b.la. farver og kontrast.</p>
            <h3>Continuity</h3>
                <p>Objekter arrangeret på linje, eller kurver opfattes som relaterede i højere grad end hvis de ikke er. Eksempelvis paragraffer, grafer (både horisontale og vertikale) kommunikerer relation.</p>
            <h3>Similarity</h3>
                <p>Visuelle elementer som er en i eksempelvis form, størrelse, retning og farve opfattes som del af en gruppe. Farver er den stærkeste måde at skabe lighed på. </p>
            <h3>Figure-Ground</h3>
                <p>Det handler om kontrasten mellem background og foreground. Det handler om at skabe så meget kontrast som muligt, med farver eller størrelse. </p>
            <h3>Common Fate</h3>
                <p>Vi ser ting der bevæger sig i samme retning som at være relateret. Ligesom fugle der flyver i samme retning er i en gruppe, mens hvis en fugl flyver i en anden retning ser vi ikke den som være en del af gruppen.</p>
            <h3>Simplicity</h3>
                <p>Simplicity hjælper med at sætte fokus på det der virkelig betyder noget. Når man holder det simpelt er det nemmere at dirigere folk mod den virkelige betydning. </p>
            <h3>Rule of Thirds</h3>
                <p>Det er baseret på princippet at det menneskelige øje bedst kan lide at se balance i design, og det giver designer en grundlinje for hvordan man skal lave designet. </p>
            <h3>Grids</h3>
                <p>Man bruger grids til at organisere indhold. Det er ikke noget man lægger mærke til når det er der, men man kan tydeligt se hvis det mangler. Det giver et gennemgående look, og bruges både på hjemmesider og i print.</p>
            <h3>White Space</h3>
                <p>White space hjælper med at øge læsbarheden, og forståelsen. Det er ’tom luft’ omkring elementer, som er med til at give overblik. Det kan adskille og knytte elementer sammen.</p>
            <h3>Symmetry</h3>
                <p>Symmetriske elementer ses som at være i samme gruppe. Hvis man deler en cirkel i halv, og der er to meget forskellige billeder, og teksturer på hver side, vil man stadig se det som at være en helhed da det er symetrisk.</p>
