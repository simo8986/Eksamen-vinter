        <p>Value proposition har to sider. Med kundeprofilen kan man kortlægge sin kundeforståelse. Med værdikortet beskriver man hvordan man har tænkt sig at skabe værdi for netop den kunde. Det handler altså om at lave et match.</p>
        <img src="billeder/kommunikation/vpc.png" alt="Value Proposition Design">
        
            <h3>Kundeprofil</h3>
            <h3>Overvej hvilke 'job' de har, der skal løses</h3>
            <p>Hvad er det, de ønsker at få gjort? E.eks. så skal en telefon kunne ringe, sende sms’er, tage billeder mm.</p>
        
            <h3>Hvilke 'pains' oplever de</h3>
            <p>Hvad fustrerer dem, hvilke costs oplever de, tid, budget osv. For telefoner er det batteritid, pris, dårlig billedkvalitet osv. </p>
        
            <h3>Hvilke gains ønsker de at opleve?</h3>
            <p>Hvad forventer de? Fra eksempelvis produktets service, design osv.</p>
        
            <h3>Værdikort(Value Map)</h3>
            <h3>Produkter og services</h3>
            <p>De produkter og services virksomheden har, eller potentielt kan udvikle, for at løse kundens ’jobs’.</p>
        
            <h3>Pain relievers</h3>
            <p>Hvordan afhjælper dine produkter og services de pains kunderne oplever?</p>
        
            <h3>Gain Creators</h3>
            <p>Hvordan hjælper dine produkter med at skabe værdi/gains for kunderne.</p>
        
            <h3>Match</h3>
            <p>Man opnår et match når du adresserer vigtige opgaver, lindrer smerter, og skaber gevinster som betyder noget for kunderne. Match er svære at finde og vedligeholde. Stræben efter match er hele formålet med designet af Value Propositions. Kunderne er dem der dømmer ens Value Proposition. </p>
