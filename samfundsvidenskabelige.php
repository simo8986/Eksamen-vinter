        <h1>Det samfundsvidenskabelige paradigme</h1>
        <p>Det samfundsvidenskabelige paradigme har fokus på afsenderen og dens placering i omverden. Det er afsenderen der bliver set som styrende i kommunikationsprocessen, og paradigmet ser kommunikation som en transmission. </p>
        <p>Der trækkes på de samfundsvidenskabelige fagområder, herunder; sociologi, samfundsvidenskab, økonomi, politik, jura m.fl. Det er ud fra de områder at metoderne og teorierne bliver dannet.</p>
        <p>Retningerne i paradigmet er systemteori og funktionalisme, der bygger på en biologisk forklaringssammenhæng.  Det er en ambition med brug af paradigmet at kunne forklare, og derefter planlægge. </p>
        <p>Sammenlignet med det humanistiske paradigme, er det samfundsvidenskabelige karakteriseret ved en tro på at verden er forudsigelig, og man kan planlægge ud fra stabile omgivelser og fast struktur. </p>
        <p>Paradigmet bygger på at verden ikke forandrer sig hurtigere, end man kan forstå helheden ved analyse af de enkelte dele. Den tror også på at en der undersøger, kan sætte sig ind i en sag fuldstændig objektivt, hvor man ikke påvirker situationen.</p>
        <p>De metoder man bruger er fokuseret på at klarlægge en årsag-virknings-sammenhæng baseret på kvantitative målinger.</p>
        <p>Teorien er ofte hentet fra amerikanske marketingstilgange, som har en økonomisk tilgang, hvor de kigger på hvordan mediernes effekt på modtageren. Paradigmet arbejder med teorier og modeller som er ældre end internettet. De bliver set som ’de gamle’. </p>
        <p>Der anvendes ofte undersøgelsesmetoder, som giver svar på målbare data, som hvor meget, eller hvor mange. Viden opbygges på baggrund af mange recipienter, og metoden er ofte deduktiv, da man slutter fra det abstrakte til det specifikke og konkrete. Den understøttes af en hypotetisk-deduktiv fremgangsmetode, hvor man først gør sig nogle teoretiske overvejelser og derefter bruger fakta til at bekræfte eller afkræfte ens antagelse. </p>
        
        <p>Karakteristiske træk i det samfundsvidenskabelige paradigme:</p>
        <ul>
            <li>Objektivitet</li>
            <li>Forklaring</li>
            <li>Kvantitativ tilgang</li>
            <li>Deduktion</li>
            <li>Stabile omgivelser</li>
            <li>Afsenderorienteret</li>
            <li>Transmission</li>
            <li>Handlingsanvisende</li>
            <li>Modtageren ses som passiv</li>
        </ul>
