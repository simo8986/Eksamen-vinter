        <h1>Peer review</h1>
        <p>Peer review hvor man vurderer sine medstuderende/kollegaer. I studieforstand er det hvor man løser en opgave, og derefter vurderer en anden studerendes opgave, mens ens egen bliver vurderet af en studerende. </p>
        <p>Man arbejder med peer review for b.la. at træne at give og modtage feedback, da det er en vigtig kompetence at have når man skal på arbejdsmarkedet. Man lærer at sætte ord på ens viden, og man bliver mere bevidst om at håndtere ens egne, og andres følelser i forhold til at give og få kritik. </p>
        <p>Når man skal give feedback er det vigtigt at det er konstruktiv kritik. Det er ikke nok bare at sige noget er dårligt, eller godt. Negativ feedback kan være nyttigt hvis det formuleres på den rigtige måde. </p>
        <p>Det kan være en god idé at starte med positiv feedback, men det kan også virke falsk og få modtageren til at sidde og vente på ’men…’, man skal rose når det giver mening. </p>
        <p>At give konstruktiv vil sige at man kommer med forbedringsforslag, og alternative forslag til hvad man kunne have gjort i stedet for. Man giver negativ feedback, men man ikke siger at det er dårligt eller forkert. Man skal anerkende den andens arbejde, og være præcis og specifik, samtidig med at man er objektiv. </p>
        <p>Når man modtager konstruktiv kritik er det fristende at begynde at forklare hvorfor man har gjort som man gjorde, men i første omgang skal man lytte og tage notater, og ikke kommentere. Først til sidst kan man stille spørgsmål og forklare hvad man gerne vil forklare, uden at gå i forsvar.</p>
